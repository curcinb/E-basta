public class Sadnica {
    private int idS;
    private String naziv;
    private String preduzece;
    private int napredak;
    private int idR;
    private int cena;
    private int kolicina;

    public int getIdS() {
        return idS;
    }

    public void setIdS(int idS) {
        this.idS = idS;
    }

    public String getNaziv() {
        return naziv;
    }

    public void setNaziv(String naziv) {
        this.naziv = naziv;
    }

    public String getPreduzece() {
        return preduzece;
    }

    public void setPreduzece(String preduzece) {
        this.preduzece = preduzece;
    }

    public int getNapredak() {
        return napredak;
    }

    public void setNapredak(int napredak) {
        this.napredak = napredak;
    }

    public int getIdR() {
        return idR;
    }

    public void setIdR(int idR) {
        this.idR = idR;
    }

    public int getCena() {
        return cena;
    }

    public void setCena(int cena) {
        this.cena = cena;
    }

    public int getKolicina() {
        return kolicina;
    }

    public void setKolicina(int kolicina) {
        this.kolicina = kolicina;
    }
    
    
}
